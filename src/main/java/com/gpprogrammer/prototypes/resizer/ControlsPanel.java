    public final void setControlsEnabled(Boolean value )
    {
        //System.out.println("setControlsEnabled");
        if(jCheckBox1.isSelected())
        {
            DimentionSelector1.setEnabled(false);
            DimentionValue1.setEnabled(false);
            if(getSizeValue() > 0)
            {
                jButton1.setEnabled(value);
            }else
            {
                jButton1.setEnabled(false);
            }
        }
        else
        {
            DimentionSelector1.setEnabled(true);
            DimentionValue1.setEnabled(true);
            if(getSizeValue() > 0 && getSizeValue1() > 0)
            {
                jButton1.setEnabled(value);
            }
            else
            {
                jButton1.setEnabled(false);
            }
        }
         jButton2.setEnabled(value);
    }
    
