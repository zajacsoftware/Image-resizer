    private static int parseInt(String input)
    {
        try 
        {
           return Integer.parseInt(input);
        } catch (NumberFormatException er) {
             System.out.println("invalid input value: "+ er.getMessage());
        }
          return 0;
    }

    public OutputData getOutputData()
    {       
        return new OutputData((DimentionSelector.getSelectedIndex() == 0 ? getSizeValue() : 0 ),
                              (DimentionSelector1.getSelectedIndex() == 1 ?  getSizeValue1() : (jCheckBox1.isSelected()?getSizeValue():0)),
                               saveType.getSelectedIndex()
                             );
    }
    
    private void init() 
    {
    //    Dimentions;
      setControlsEnabled(false);
      DocumentFilter fl = new DocumentFilter() {
            Pattern regEx = Pattern.compile("\\d+");
            @Override
            public void replace(FilterBypass fb, int offset, int length, String text, AttributeSet attrs) throws BadLocationException {  
                Matcher matcher = regEx.matcher(text);
                if (text!="" && !matcher.matches()) {
                    return;
                }
                super.replace(fb, offset, length, text, attrs);
               // textVauesChanged();
            }
        };
      DocumentListener dl = new DocumentListener(){
          @Override
          public void insertUpdate(DocumentEvent e) {
            textVauesChanged();
          }

          @Override
          public void removeUpdate(DocumentEvent e) {
            textVauesChanged();
          }

          @Override
          public void changedUpdate(DocumentEvent e) {
            textVauesChanged();
          }
      };

      ((AbstractDocument) DimentionValue.getDocument()).setDocumentFilter(fl);
      ((AbstractDocument) DimentionValue.getDocument()).addDocumentListener(dl);
      ((AbstractDocument) DimentionValue1.getDocument()).setDocumentFilter(fl); 
      ((AbstractDocument) DimentionValue1.getDocument()).addDocumentListener(dl);
    }
    
    private void textVauesChanged()
    {
        if(assetsReady)
        {
           setControlsEnabled(true);
        }
    }
